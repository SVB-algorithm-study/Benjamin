def birthday(s, d, m):
    # Write your code here
    count = 0
    start = 0
    w_sum = 0
    for end in range(len(s)):
        w_sum += s[end]
        if end + 1 >= m:
            if w_sum == d:
                count += 1
            w_sum -= s[start]
            start += 1
    return count
            
    
