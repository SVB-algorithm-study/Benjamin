def pangrams(s):
    # Write your code here
    alphabet = [False] * 26
    for c in s:
        if c == ' ': continue
        index = ord(c.lower()) - ord('a')
        # print(c.lower(), index)
        alphabet[index] = True
    
    if False in alphabet: return "not pangram"
    else: return "pangram"

